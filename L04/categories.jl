# ╔═╡ 84fec8d4-580d-4ae6-8519-fef1715e4c55
@model function busy_m_SWH(sex, height, mean_height, weight)
	n_groups = length(unique(sex))
	α ~ filldist(Normal(60, 10), n_groups)
	β ~ filldist(Uniform(0, 1), n_groups)
	σ ~ Uniform(0, 10)

	μ = @. α[sex] + β[sex] * (height - mean_height)
	weight ~ MvNormal(μ, σ^2 * I)
end

