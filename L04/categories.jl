# ╔═╡ f0f18712-4642-4f07-accc-7027c79aed37
m_spline_prior = splines(basis, cherries.year);

# ╔═╡ c614d836-8bd9-400e-affe-7f24b56f4b2b
m_spline_cond = m_spline_prior | (y=Float64.(cherries.doy),);

# ╔═╡ a9a06c89-1678-4751-b51d-0bb2007714c7
chn_splines = sample(m_spline_cond, NUTS(), 100);

