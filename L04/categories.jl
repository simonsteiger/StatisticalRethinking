# ╔═╡ bb5c4ba0-4c45-4fce-a28a-e810d1c38b47
md"""
### Working with splines
This model uses the package [`BSplines`](https://github.com/sostock/BSplines.jl), and we make use of the functions `BSplineBasis` and `Spline`.

In our `Turing` model, we include an intercept `α`, a random error `σ`, and `Normal` priors for each of the spline coefficients `w`. Instead of writing `MvNormal(zeros(length(basis)), I * 10)` we could have also written `filldist(Normal(0, 10), length(basis))`, where `length(basis)` is the number of knots in our spline.
"""

# ╔═╡ 16e710fc-7445-4f6f-aaac-f87dc4157851
@model function splines(basis, x)
	α ~ Normal(100, 1)
	σ ~ truncated(Normal(0, 10), lower=0)
	w ~ MvNormal(zeros(length(basis)), I * 10)
	s = Spline(basis, w)
	μ = @. α + s(x)
	y ~ MvNormal(μ, σ^2 * I)
	return (; α, w, μ, y)
end

# ╔═╡ 12b0bf0d-4414-47a5-9a14-c17bc2279ede
md"""
We select the number of knots and place them equally spaced across the range of the input data.
"""

# ╔═╡ 5df744fd-75a2-4580-90d2-0a8fe8eb0934
num_knots = 30

# ╔═╡ 76c7a335-0db5-4e4e-a4d7-9c55dd7ff460
knots_seq = let year = cherries.year
	range(minimum(year), maximum(year); length=num_knots)
end

# ╔═╡ c71b725d-afd9-474e-924d-c83b409d65eb
md"""
We then create a BSplineBasis variable by passing the `order` and `breakpoints` (knot positions).
