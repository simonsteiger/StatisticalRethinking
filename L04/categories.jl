# ╔═╡ 4d7cb5cf-d903-481e-9d1b-ae0a4de44fe6
let go_poly
	# Simulate the raw data
	x = rand(Uniform(-3, 3), 8)
	y = rand.(Normal.(x, 2))

	# Set up prior model and sample from it
	m_prior = polynomial(x, poly_deg)
	β_prior = reduce(hcat, [m_prior().β for _ in 1:4])
	prior_pred_poly = map(eachcol(β_prior)) do col
		npoly.(Ref(col), -5:0.1:5)
	end

	# Condition on observations and sample
	m_cond = m_prior | (y=y,)
	chn = sample(m_cond, NUTS(), 1000)
	βs = reduce(hcat, get_params(chn).β)
	post_pred_poly = map(eachrow(βs)) do row
		npoly.(Ref(row), -5:0.1:5)
	end |> x -> reduce(hcat, x)
	qs = [quantile(row, [0.1, 0.5, 0.9]) for row in eachrow(post_pred_poly)]

	# Create figure
	fig = Figure(size=(680, 340))

	# Prior panel
	ax1 = Axis(fig[1, 1], limits=((-5, 5), (-5, 5)), title="Prior samples")
	lines!.(Ref(-5:0.1:5), prior_pred_poly)

	# Posterior panel
	ax2 = Axis(fig[1, 2], limits=((-5, 5), (-5, 5)), title="Posterior samples")
	band!(-5:0.1:5, sdim(1)(qs), sdim(3)(qs), color=(colors[2], 0.3))
	lines!(-5:0.1:5, sdim(2)(qs), color=:white, linewidth=5)
	lines!(-5:0.1:5, sdim(2)(qs), color=colors[2], linewidth=2)
	scatter!(x, y, color=:transparent, strokewidth=2, strokecolor=colors[1])
	
