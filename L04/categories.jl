# ╔═╡ 4a74409d-6d51-458f-9884-24cab0a3eb82
md"""
## Polynomials

Richard McElreath mentions that he does not suggest using polynomials because of their rigid behaviors which often contradict our domain knowledge:
- Polynomials enforce strong symmetry, which is usually is contrary to what would be scientifically reasonable
- Any point can influence the shape of the polynomial arbitrarily far away from it
- There is explosive uncertainty at the edges of the input data range

[Link](https://youtu.be/F0N4b7K_iYQ?si=1rbeSClboJGDITPE&t=3135) to video segment.
"""

# ╔═╡ 7fec5918-a56f-412e-a585-8c60b6d1c35c
npoly(β, x) = mapreduce(i -> β[i] * x^(i-1), +, 1:length(β))

# ╔═╡ 1faccda7-51d4-40a6-ade0-8e8c53988cd9
@model function polynomial(x, deg)
	β ~ MvNormal(zeros(deg + 1), I)
	σ ~ Exponential(1)
	μ = npoly.(Ref(β), x)
	y ~ MvNormal(μ, σ^2 * I)
	return(; β, y)
end

# ╔═╡ 0ff6d735-d074-4ddd-86fb-45176d4509ea
md"""
